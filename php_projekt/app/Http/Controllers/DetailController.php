    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($maincat,$subcat,$id)
    {
        $product = Product::with(['image','maincat','subcat','maker','sizes'])
            ->whereHas('maincat',function ($q) use ($maincat){
                $q->where('main_cats.route_name','=', $maincat);
            })->whereHas('subcat',function ($q) use ($subcat){
                $q->where('subcats.route_name','=', $subcat);
            })->find($id);
        //$product = Product::with(['image','sizes','maincat','subcat'])->find($id);
        return view('detail',['product' => $product]);
    }

